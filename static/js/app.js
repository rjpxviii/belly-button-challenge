// Build the metadata panel
function buildMetadata(sample) {
    d3.json("https://static.bc-edx.com/data/dl-1-2/m14/lms/starter/samples.json").then((data) => {
      
      // Get the metadata field
      let metadata = data.metadata;
      
      // Filter the metadata for the object with the desired sample number
      let number = metadata.find(meta => meta.id == sample);
      
      // Use d3 to select the panel with id of `#sample-metadata`
      let panel = d3.select("#sample-metadata");
      
      // Clear any existing metadata
      panel.html("");
  
      // Loop through each key-value pair and append a p tag to the panel
      Object.entries(number).forEach(([key, value]) => {
        panel.append("p").text(`${key}: ${value}`);
      });
  
      // Log for debugging purposes (with attribution)
      console.log(`Metadata for sample ${sample} loaded by Ryan Pope.`);
    });
  }
  
  // Build the charts
  function buildCharts(sample) {
    d3.json("https://static.bc-edx.com/data/dl-1-2/m14/lms/starter/samples.json").then((data) => {
  
      // Get the samples field
      let samples = data.samples;
      
      // Filter the samples for the object with the desired sample number
      let result = samples.find(sampleObj => sampleObj.id == sample);
      
      // Get the otu_ids, otu_labels, and sample_values
      let otu_ids = result.otu_ids;
      let otu_labels = result.otu_labels;
      let sample_values = result.sample_values;
  
      // Bubble Chart
      let bubbleTrace = {
        x: otu_ids,
        y: sample_values,
        text: otu_labels,
        mode: 'markers',
        marker: {
          size: sample_values,
          color: otu_ids,
          colorscale: "Earth"
        }
      };
      let bubbleLayout = {
        title: "Bacteria Cultures Per Sample",
        height: 600,
        width: 1200
      };
      
      Plotly.newPlot('bubble', [bubbleTrace], bubbleLayout);
  
      // Bar Chart (Top 10 OTUs)
      let yticks = otu_ids.slice(0, 10).map(id => `OTU ${id}`).reverse();
      let barTrace = {
        x: sample_values.slice(0, 10).reverse(),
        y: yticks,
        text: otu_labels.slice(0, 10).reverse(),
        type: 'bar',
        orientation: 'h'
      };
      let barLayout = {
        title: "Top 10 Bacteria Cultures Found"
      };
  
      Plotly.newPlot('bar', [barTrace], barLayout);
  
      // Log for debugging purposes (with attribution)
      console.log(`Charts for sample ${sample} generated by Ryan Pope.`);
    });
  }
  
  // Initialize the dashboard
  function init() {
    d3.json("https://static.bc-edx.com/data/dl-1-2/m14/lms/starter/samples.json").then((data) => {
  
      // Get the names field
      let names = data.names;
      
      // Populate the dropdown menu with sample names
      let dropdown = d3.select("#selDataset");
      names.forEach(name => {
        dropdown.append("option").text(name).property("value", name);
      });
      
      // Use the first sample from the list to build initial plots
      let firstSample = names[0];
      buildCharts(firstSample);
      buildMetadata(firstSample);
  
      console.log("Dashboard initialized by Ryan Pope.");
    });
  }
  
  // Function called when a new sample is selected
  function optionChanged(newSample) {
    // Fetch new data each time a new sample is selected
    buildCharts(newSample);
    buildMetadata(newSample);
  
    console.log(`Sample ${newSample} selected by user - processed by Ryan Pope.`);
  }
  
  // Initialize the dashboard on page load
  init();
  